package animalplanet_v2;

public abstract class Animal {
    protected int idade;
    protected float peso;

    public abstract void emitirSom();
    
    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }
}
